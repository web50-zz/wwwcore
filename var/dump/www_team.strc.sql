CREATE TABLE `www_team` (
  `id` mediumint(8) unsigned NOT NULL AUTO_INCREMENT,
  `title` varchar(255) NOT NULL DEFAULT '',
  `title_eng` varchar(255) NOT NULL DEFAULT '',
  `position` varchar(255) NOT NULL,
  `position_eng` varchar(255) NOT NULL DEFAULT '',
  `content` text NOT NULL,
  `content_eng` text NOT NULL,
  `image` varchar(255) NOT NULL DEFAULT '',
  `uri` varchar(255) NOT NULL DEFAULT '',
  `address` varchar(255) NOT NULL,
  `address_eng` varchar(255) NOT NULL,
  `mobile_phone` varchar(100) NOT NULL,
  `phone_fax` varchar(100) NOT NULL,
  `email` varchar(100) NOT NULL,
  `site` varchar(100) NOT NULL,
  `titles` varchar(255) NOT NULL,
  `titles_eng` varchar(255) NOT NULL,
  `practices` varchar(255) NOT NULL,
  `practices_eng` varchar(255) NOT NULL,
  `directions` varchar(255) NOT NULL,
  `directions_eng` varchar(255) NOT NULL,
  PRIMARY KEY (`id`,`image`)
) ENGINE=MyISAM AUTO_INCREMENT=8 DEFAULT CHARSET=utf8
